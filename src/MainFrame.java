    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:
        String scheduleCourse = (String) scheduleCourseCodeComBox.getSelectedItem();
        String scheduleStudent = (String) studentNameComBox.getSelectedItem();
        String[] scheduleArray = scheduleStudent.split(", ", 3);
        String status = "";
        if (ScheduleQueries.getScheduledStudentCount(currentSemester, scheduleCourse) < CourseQueries.getCourseSeats(currentSemester, scheduleCourse)){
            status = "S";
            courseScheduledMsg.setText(scheduleCourse + " has been scheduled");
        }else{
            status = "W";
            courseScheduledMsg.setText(scheduleCourse + " was waitlisted");
        }
        
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        ScheduleEntry entry = new ScheduleEntry(currentSemester,scheduleCourse, scheduleArray[2], status, currentTimestamp);
        ScheduleQueries.addScheduleEntry(entry);
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

