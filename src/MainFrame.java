    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes(){
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemesterLabel.getText());
        String[] courseInfo = new String[courses.size()];
        
        for(int i = 0; i < courseInfo.length; i++){
            CourseEntry entry = courses.get(i);
            courseInfo[i] = entry.getCourseCode();
        }
        scheduleCourseCodeComBox.setModel(new javax.swing.DefaultComboBoxModel(courseInfo));
        dispCourseListComBox.setModel(new javax.swing.DefaultComboBoxModel(courseInfo));
        dropCourseAdminComBox.setModel(new javax.swing.DefaultComboBoxModel(courseInfo));
        dropCourseStudentComBox1.setModel(new javax.swing.DefaultComboBoxModel(courseInfo));
    }
    
    public void rebuildStudentComboBoxes(){
        //array list, array of students, fill student array with info to be displayed in combo box, set model for both student combo boxes
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String[] studentInfo = new String[students.size()];
        
        for (int i = 0; i < studentInfo.length; i++){
            StudentEntry studentEnt = students.get(i);
            studentInfo[i] = studentEnt.getLastName() + ", " + studentEnt.getFirstName() + ", " + studentEnt.getStudentID();
        }
        studentNameComBox.setModel(new javax.swing.DefaultComboBoxModel(studentInfo));
        displayScheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentInfo));
        dropStudentComBox.setModel(new javax.swing.DefaultComboBoxModel(studentInfo));
        dropCourseStudentComBox2.setModel(new javax.swing.DefaultComboBoxModel(studentInfo));
    }

