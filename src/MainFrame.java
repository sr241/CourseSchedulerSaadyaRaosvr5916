    private void dropCourseStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseStudentBtnActionPerformed
        // TODO add your handling code here:
        dropCourseStudentTxtArea.setText("");
        String courseTBD = (String) dropCourseStudentComBox1.getSelectedItem();
        String currStudent = (String) dropCourseStudentComBox2.getSelectedItem();
        String[] studentArr = currStudent.split(", ", 3);
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, studentArr[2], courseTBD);
        dropCourseStudentTxtArea.append(studentArr[0] + ", " + studentArr[1] + " has been dropped from " + courseTBD + "\n");
        ArrayList<ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseTBD);
        if (waitlist.size() > 0){
            ScheduleQueries.updateScheduleEntry(currentSemester, waitlist.get(0));
            StudentEntry stud = StudentQueries.getStudent(waitlist.get(0).getStudentID());
            dropCourseStudentTxtArea.append(stud.getLastName() + ", " + stud.getFirstName() + " has been scheduled into " + courseTBD + "\n");
        }
        displayScheduleButtonActionPerformed(null);
    }//GEN-LAST:event_dropCourseStudentBtnActionPerformed

    private void dropCourseAdminBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseAdminBtnActionPerformed
        // TODO add your handling code here:
        dropCourseAdminTxtArea.setText("");
        String studentSch = "Scheduled students dropped from the course:\n";
        String studentWait = "Waitlisted students dropped from the course:\n";
        StudentEntry currStud;
        for (ScheduleEntry entry : ScheduleQueries.getScheduledStudentsByCourse(currentSemester, (String) dropCourseAdminComBox.getSelectedItem())){
            currStud = StudentQueries.getStudent(entry.getStudentID());
            studentSch += currStud.getLastName() + ", " + currStud.getFirstName() + " " + currStud.getStudentID() + "\n";
        }
        for (ScheduleEntry entry : ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, (String) dropCourseAdminComBox.getSelectedItem())){
            currStud = StudentQueries.getStudent(entry.getStudentID());
            studentWait += currStud.getLastName() + ", " + currStud.getFirstName() + " " + currStud.getStudentID() + "\n";
        }
        ScheduleQueries.dropScheduleByCourse(currentSemester, (String)dropCourseAdminComBox.getSelectedItem());
        CourseQueries.dropCourse(currentSemester, (String)dropCourseAdminComBox.getSelectedItem());
        dropCourseAdminTxtArea.append(studentSch + "\n");
        dropCourseAdminTxtArea.append(studentWait);

        rebuildCourseComboBoxes();
        
    }//GEN-LAST:event_dropCourseAdminBtnActionPerformed

    private void dropStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentBtnActionPerformed
        // TODO add your handling code here:
        dropStudentTxtArea.setText("");
        String studentTBD = (String) dropStudentComBox.getSelectedItem();
        String[] studentArr = studentTBD.split(", ", 3);
        StudentEntry stud = StudentQueries.getStudent(studentArr[2]);
        dropStudentTxtArea.append((String) dropStudentComBox.getSelectedItem() + " has been dropped from the list of students.\n\n");
        for (String currSemester : SemesterQueries.getSemesterList()){
            String dropped = "";
            String scheduled = "";
            for (ScheduleEntry currEntry : ScheduleQueries.getScheduleByStudent(currSemester, studentArr[2])){
                ScheduleQueries.dropStudentScheduleByCourse(currSemester, studentArr[2], currEntry.getCourseCode());
                if (currEntry.getStatus().equals("W"))
                    dropped += ((String) dropStudentComBox.getSelectedItem() + " has been dropped from the waitlist for " + currEntry.getCourseCode() + "\n");
                else
                    dropped += ((String) dropStudentComBox.getSelectedItem() + " has been dropped from " + currEntry.getCourseCode() + "\n");
                
                ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currSemester, currEntry.getCourseCode());
                if (waitlistedStudents.size() > 0){
                    ScheduleQueries.updateScheduleEntry(currSemester, waitlistedStudents.get(0));
                    StudentEntry schedStudent = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());
                    scheduled += schedStudent.getLastName() + ", " + schedStudent.getLastName() + " " + waitlistedStudents.get(0).getStudentID() + " has been scheduled into " + waitlistedStudents.get(0).getCourseCode() + "\n";
                }
            }
            if (!dropped.isEmpty() || !scheduled.isEmpty()){
                dropStudentTxtArea.append("For Semester: " + currSemester + "\n");
                dropStudentTxtArea.append(dropped);
                dropStudentTxtArea.append(scheduled);
            }
        }
        StudentQueries.dropStudent(studentArr[2]);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentBtnActionPerformed

    private void dispStudentsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dispStudentsBtnActionPerformed
        // TODO add your handling code here:
        ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, (String)dispCourseListComBox.getSelectedItem());
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, (String)dispCourseListComBox.getSelectedItem());

        DefaultTableModel tableS = (DefaultTableModel)dispSchedStudTable.getModel();
        tableS.setNumRows(0);
        Object[] rowDataS = new Object[3];
        for (ScheduleEntry entry : scheduled)
        {
            StudentEntry curr = StudentQueries.getStudent(entry.getStudentID());
            rowDataS[0] = curr.getLastName();
            rowDataS[1] = curr.getFirstName();
            rowDataS[2] = entry.getStudentID();
            tableS.addRow(rowDataS);
        }

        DefaultTableModel tableW = (DefaultTableModel)dispWaitStudTable.getModel();
        tableW.setNumRows(0);
        Object[] rowDataW = new Object[3];
        for (ScheduleEntry entry : waitlisted)
        {
            StudentEntry curr = StudentQueries.getStudent(entry.getStudentID());
            rowDataW[0] = curr.getLastName();
            rowDataW[1] = curr.getFirstName();
            rowDataW[2] = entry.getStudentID();
            tableW.addRow(rowDataW);
        }
    }//GEN-LAST:event_dispStudentsBtnActionPerformed

